package com.shahmeer.ios_adaptive_context_menu

import io.flutter.embedding.engine.plugins.FlutterPlugin

/**
 * Android plugin entry point.
 *
 * This package uses Flutter-side menu rendering on Android, so no native
 * method-channel behavior is required here.
 */
class IosAdaptiveContextMenuPlugin : FlutterPlugin {
    override fun onAttachedToEngine(binding: FlutterPlugin.FlutterPluginBinding) {}

    override fun onDetachedFromEngine(binding: FlutterPlugin.FlutterPluginBinding) {}
}
